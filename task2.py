humidity_value = 50 # Humidity_value variable is assigned a value in percentage
if humidity_value < 30: #humidity less than 30 percentage
    print("It is Dry")
elif humidity_value > 60:# humidity greater than 60 percentage
    print("High Humidity")
else:
    print("It's OK")
